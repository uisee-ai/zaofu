"""Static workflow event/problem registry.

The registry is a code-level contract between workflow event producers and
runtime consumers. It does not replace events.jsonl truth or zf.yaml control
plane; it only classifies emitted events so Supervisor, Run Manager, and
Autoresearch do not each maintain divergent event-name tables.
"""

from __future__ import annotations

from dataclasses import dataclass


