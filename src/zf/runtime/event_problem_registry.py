@dataclass(frozen=True)
class EventProblemSpec:
    event_type: str
    event_class: str
    problem_class: str = ""
    failure_class: str = ""
    source: str = "workflow_event"
    severity: str = "medium"
    title: str = ""
    owner_route: str = ""
    action_policy: str = ""
    intervention_class: str = ""
    suggested_route: str = ""
    suggested_action_kind: str = ""
    supervisor_attention: str = "none"
    run_manager_semantics: tuple[str, ...] = ()
    autoresearch_eligible: bool = False
    projection_visibility: bool = True
    producer_kind: str = ""
    required_scope_any: tuple[str, ...] = ()
    notification_policy: str = ""
    recovery_policy: str = ""
    dedupe_key_fields: tuple[str, ...] = ()
    human_required_when: tuple[str, ...] = ()

    @property
    def is_expected_negative(self) -> bool:
        return self.event_class == "expected_negative"

    @property
    def is_abnormal(self) -> bool:
        return self.event_class == "abnormal"

    @property
    def is_projection_only(self) -> bool:
        return self.event_class == "projection_only"

    @property
    def effective_notification_policy(self) -> str:
        if self.notification_policy:
            return self.notification_policy
        if self.action_policy == "human_escalate" or self.owner_route in {
            "human",
            "owner_notify",
        }:
            return "owner_immediate"
        if self.owner_route in {"run_manager", "autoresearch"}:
            return "owner_on_repair_failed"
        return "trace_only" if self.is_projection_only else "run_manager_first"

    @property
    def effective_recovery_policy(self) -> str:
        if self.recovery_policy:
            return self.recovery_policy
        if self.action_policy == "human_escalate" or self.owner_route == "human":
            return "human"
        if self.owner_route == "autoresearch" or self.autoresearch_eligible:
            return "run_manager_then_autoresearch"
        if self.owner_route == "run_manager":
            return "run_manager"
        return "none"


def _candidate_quality_expected(event_type: str, title: str = "") -> EventProblemSpec:
    post_terminal_events = {
        "candidate.quality.failed",
        "integration.failed",
        "verify.failed",
        "test.failed",
        "judge.failed",
    }
    return EventProblemSpec(
        event_type=event_type,
        event_class="expected_negative",
        problem_class="candidate_quality",
        failure_class=event_type.replace(".", "_"),
        source="workflow_branch",
        title=title or event_type,
        owner_route="orchestrator_replan",
        action_policy="needs_diagnosis",
        intervention_class="semantic_replan",
        suggested_route="workflow_rework",
        suggested_action_kind="follow_workflow_rework",
        supervisor_attention="on_repeated",
        run_manager_semantics=(
            ("post_terminal_work",) if event_type in post_terminal_events else ()
        ),
        autoresearch_eligible=event_type in {
            "candidate.quality.failed",
            "integration.failed",
            "verify.failed",
            "test.failed",
            "judge.failed",
        },
        producer_kind="workflow_stage",
        required_scope_any=("task_id", "candidate_ref", "pdd_id", "trace_id"),
    )


