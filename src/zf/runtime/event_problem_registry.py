def event_consumer_contract_gaps(event_types: set[str]) -> list[str]:
    """Return event types that look actionable but lack a registry consumer.

    This intentionally focuses on workflow/problem events rather than all
    telemetry. A role may publish a project-specific info event without a
    runtime consumer; failure/block/reject events are the dangerous class.
    """

    gaps: list[str] = []
    for event_type in sorted(str(item or "").strip() for item in event_types):
        if not event_type:
            continue
        if not looks_actionable_event(event_type):
            continue
        spec = spec_for_event(event_type)
        if spec is None or (
            spec.is_projection_only
            and spec.supervisor_attention == "none"
            and not spec.autoresearch_eligible
            and "pending_action" not in spec.run_manager_semantics
        ):
            gaps.append(event_type)
    return gaps


def looks_actionable_event(event_type: str) -> bool:
    return any(
        event_type.endswith(suffix)
        for suffix in (
            ".failed",
            ".blocked",
            ".rejected",
            ".suspended",
            ".missing",
        )
    )


def actionable_event_types(event_types: set[str]) -> set[str]:
    return {
        event_type
        for event_type in (str(item or "").strip() for item in event_types)
        if event_type and looks_actionable_event(event_type)
    }


